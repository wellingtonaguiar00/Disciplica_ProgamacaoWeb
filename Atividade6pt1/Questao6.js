// Imprimir os números um abaixo do outro
console.log("Números de 1 a 20 abaixo um do outro:");
for (let i = 1; i <= 20; i++) {
    console.log(i);
}

// Imprimir os números um ao lado do outro
console.log("Números de 1 a 20 ao lado um do outro:");
let numerosLadoALado = "";
for (let i = 1; i <= 20; i++) {
    numerosLadoALado += i + " ";
}
console.log(numerosLadoALado);
